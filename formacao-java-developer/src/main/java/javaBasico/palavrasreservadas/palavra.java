package javaBasico.palavrasreservadas;

public class palavra {
    /*
        Controle de Pacotes.

        import: Importa pacotes ou classes para dentro do código.
        package: Especifica a que pacote todas as classes de um arquivo pertencem.


        Modificadores de Acesso.

        public: Classes, atributos e metodos com acesso publico.
        private: Classes, atributos e metodos com acesso somente na sua classe.
        protected: Acesso por herança. ou seja, por classes no mesmo pacote e subclasses.


        Tipos Primitivos.

        char, int, float e etc.


        Modificadores de classes, variáveis ou métodos.

        abstract: Classe que não pode ser intanciada ou metodo que precisa ser implementado
                  por uma subclasse nao abstrata.
        class: Especifica uma classe
        extends: Indica a super classe (pai) que a subclasse (filha) está está estendendo.
        final: Impossibilita que uma classe seja estendida, que um metodo seja sobrescrito ou
               que uma variavel seja reinicializada.
        implements: Indica as interfaces que uma classe ira implementar.
        interface: Especifica uma interface.
        new: Instancia um novo objeto, chamado seu construtor.
        static: Faz um metodo ou variavel pertencer a classe ao inves das instancias.
        synchronized: Indica que um metodo so pode ser acessado por uma thread de cada vez.
        transient: Impede a serialização de campos.


    */
}